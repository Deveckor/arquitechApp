import React, {useState} from 'react'
import TextField from '@mui/material/TextField'
import {useForm} from 'react-hook-form'
import Box from '@mui/material/Box';
import styles from '../styles/pages/createAccount.module.scss';
import Button from '@mui/material/Button';
import Typography from '@mui/material/Typography';
import {URL_BASE} from '../services/config';
import ModalInfo from '../components/ModalInfo'
import Head from 'next/head';
import {createAction, loginAction} from '../actions/crudOfficeActions'
import {useSelector, useDispatch} from 'react-redux'
import { helpHttp } from "../helpers/helpHttp";

const InputLogin = () =>{

  const state = useSelector((state) => state);
  const dispatch = useDispatch();
  const {dt} = state.office;

  const [error, setError] = useState(null)
  const [data, setData] = useState()
  const [open, setOpen] = useState(false);
  const handleOpen = () => setOpen(true);
  const handleClose = () => setOpen(false);

  const {register, handleSubmit, formState} = useForm({mode: 'onChange'}); 

  let api = helpHttp()

  const createOfficeAccount = async (data) =>{
    
    let options = {
      body: data,
      headers: { 
        "content-type": "application/json"
      }
    };
    try {
      let res = await api.post(`${URL_BASE}/offices/createAccount`, options);
      console.log(res);
      if(!res.err) 
      handleOpen()
      dispatch(createAction(res.office));
      
    } catch (error) {
      handleOpen()
      setError(error)
    }
  }

  const onSubmit = async (data) => {
    
    data = {
      ...data,
      "bucket": data.email,
    }
    
    createOfficeAccount(data)
    
   
  }
  if(state.office.dt.length > 0){
    console.log(state.office.dt[0].name);
  }
  console.log(state.office.dt);
  
  return (
    <>
    <Head>
        <title>Crear Cuenta</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
    <Box
    className={styles.box}
    component="form"
    autoComplete='on'
    onSubmit={handleSubmit(onSubmit)}
    >
     <Typography
            noWrap           
            sx={{
              fontFamily: 'Montserrat',
              fontWeight:  700,
              letterSpacing:{ xs: '.1rem', md: '.3rem'},
              color: 'inherit',
              textDecoration: 'none',
              fontSize:'18px'
            }}
          >
            Bienvenido a Arquitech
          </Typography>
          <Typography
                    
                    sx={{
                      
                      fontFamily: 'Montserrat',
                      fontWeight:  700,
                      color: 'inherit',
                      textDecoration: 'none',
                      fontSize:'12px'
                    }}
                    >
            Crea tu cuenta:
          </Typography>
     <TextField
        required
       
        label='Nombre Compañia:'
        
        inputProps={{ pattern: "^[a-zA-ZñÑáÁÉéÍíÓóÚú. ]*$" }}
        {...register('name', {required: 'error text'})}
        />
      <TextField
        required
        
        label='Email:'
        
        inputProps={{ pattern: "^([\da-z_\.-]+)@([\da-z\.-]+)\.([a-z\.]{2,6})$" }}
        {...register('email', {required: 'error text'})}
        />
      <TextField
        
        label='Dirección:'
        {...register('address')}
        />
      <TextField
        required
        id="outlined-required"
        label='RFC:'
        
        
        {...register('rfc', {required: 'error text'})}
        />
      <TextField
        required
        type="password"
        
        label='Password:'
        {...register('password', {required: 'error text'})}
        />
      <Button
      type="submit"
      value="Submit"
      variant="outlined"
      disabled={!formState.isValid}  
      >Enviar</Button>
   
      <ModalInfo
      handleClose={handleClose}
      open={open}
      handleOpen={handleOpen}
      data={data}
      />
    </Box>
      </>
  )
}

export default function createAccount() {
  
  return (
    <div className={styles.container}>
      <InputLogin/>
    </div>
  )
}
